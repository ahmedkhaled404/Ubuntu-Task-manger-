    private void getTableIntialized(String command) throws IOException {
        String[] lines = TaskManager.strFormatter(TaskManager.bashTerminal(command));
        
        String[] tableHeaders = TaskManager.splice(lines[3].split(" "), 0);
        int length = tableHeaders.length;

        boolean[] cell_editable = new boolean[length];
        for (int i = 0; i < length; i++) {
            cell_editable[i] = false;
        }

        jTable.setModel(new javax.swing.table.DefaultTableModel(
                new Object[][]{},
                tableHeaders
        ) {
            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return cell_editable[columnIndex];
            }
        });

        DefaultTableModel model = (DefaultTableModel) jTable.getModel();
        for (int i = 5; i < lines.length; i++) {
            model.addRow(TaskManager.splice(lines[i].split(" "), 0));
        }

        jLabel_PckgCount.setText(model.getRowCount() + " Installed Package");
        jTable.getColumnModel().getColumn(0).setPreferredWidth(200);
    }

