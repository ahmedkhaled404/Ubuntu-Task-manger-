    private void jTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMouseClicked
        try {
            int row_index = jTable.getSelectedRow();
            if (row_index > -1) {
                TableModel model = jTable.getModel();

                this.packageName = model.getValueAt(row_index, 0).toString();
                String version = model.getValueAt(row_index, 1).toString();
                String architecture = model.getValueAt(row_index, 2).toString();
                String description = model.getValueAt(row_index, 3).toString();

                jLabel_Pckg.setText("Package [ " + this.packageName + " ] : Version [ " + version + " ] : Arch [ " + architecture + " ] : Desc [ " + description + " ]");
                jButton_Uninstall.setEnabled(true);
            }
        } catch (NumberFormatException ex) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_jTableMouseClicked

