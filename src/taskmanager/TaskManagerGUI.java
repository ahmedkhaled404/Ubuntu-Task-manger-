    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            int pid = getProcessID();
            String oldState = getProcessState(pid);
            String signal = (String) (JComboBoxSignal.getSelectedItem());
            String parameter = TaskManager.bashTerminal("kill -l " + signal);

            if (!oldState.equals("")) {
                signalAProcess(parameter, pid);
                String newState = getProcessState(pid);

                JOptionPane.showMessageDialog(null, "Done.\nThe process status is < " + (newState.equals("") ? notFoundMSG : newState) + " >.", "Info", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, notFoundMSG, "Info", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Please Enter a valid PID.", "Warning", JOptionPane.WARNING_MESSAGE);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, ex, "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void packageMng(String cmd, String packageName, String option) {
        if (!packageName.equals("")) {
            JOptionPane.showMessageDialog(null, "Hold on, it takes time.\nDon't go away", "Message", JOptionPane.INFORMATION_MESSAGE);
            TerminalWindow terminal = new TerminalWindow(cmd + " " + packageName + " " + option);
            terminal.show();
        } else {
            JOptionPane.showMessageDialog(null, "Please Enter Specify a Package Name", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }
