    private String getProcessState(int pid) throws IOException {
        return TaskManager.bashTerminal("ps -o state= -p " + pid); //RETURNS STATE OF A PROCESS BY ID
    }

    private int getProcessID() {
        return Integer.parseInt(jTextFieldPID.getText());
    }

    private void signalAProcess(String signal, int pid) throws IOException {
        TaskManager.bash(TaskManagerGUI.rootCommand + "kill -" + signal + " " + pid);
    }

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            int pid = getProcessID();
            String oldState = getProcessState(pid);

            if (!oldState.equals("") && !oldState.equals("T")) {
                signalAProcess("19", pid);
                String newState = getProcessState(pid);

                JOptionPane.showMessageDialog(null, "Done.\nThe process status is < " + newState + " >.", "Info", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, !oldState.equals("") ? "Already stopped.\nThe process status is < " + oldState + " >." : notFoundMSG, "Info", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Please Enter a valid PID.", "Warning", JOptionPane.WARNING_MESSAGE);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, ex, "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

